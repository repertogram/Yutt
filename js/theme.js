// ==================== МОДУЛЬ ПЕРЕКЛЮЧЕНИЯ ТЕМЫ (THEME.JS) ====================
// Этот скрипт позволяет переключать сайт между светлой и тёмной темой.
// Выбор сохраняется в localStorage, поэтому при обновлении страницы тема не сбрасывается.

// Весь код обёрнут в самовызывающуюся функцию (IIFE – Immediately Invoked Function Expression).
// Это нужно для того, чтобы переменные внутри неё не мешали другим скриптам на странице.
(function() {
    // ---------- 1. НАХОДИМ КНОПКУ ПЕРЕКЛЮЧЕНИЯ ТЕМЫ ----------
    // Получаем элемент кнопки по её id="themeToggle". Эта кнопка есть в шапке всех страниц.
    const themeToggle = document.getElementById('themeToggle');
    
    // Если кнопка по какой-то причине не найдена (например, забыли добавить в HTML),
    // то дальнейшее выполнение скрипта бессмысленно – выходим из функции.
    if (!themeToggle) return;

    // ---------- 2. ВОССТАНАВЛИВАЕМ ТЕМУ ПРИ ЗАГРУЗКЕ ----------
    // Пытаемся прочитать из localStorage значение по ключу 'theme'.
    // Оно может быть либо 'dark' (тёмная тема), либо 'light' (светлая), либо отсутствовать.
    const savedTheme = localStorage.getItem('theme');
    
    // Если ранее пользователь выбрал тёмную тему (в localStorage лежит 'dark')
    if (savedTheme === 'dark') {
        // Добавляем к тегу <body> класс 'dark-theme'. Все стили для тёмной темы
        // в файле style.css прописаны с использованием этого класса (например, body.dark-theme).
        document.body.classList.add('dark-theme');
        // Меняем текст на кнопке – теперь она предлагает переключиться на светлую тему.
        themeToggle.textContent = '☀️ Светлая';
    } else {
        // Иначе (если тема светлая или запись отсутствует) – оставляем светлую тему.
        // На всякий случай явно удаляем класс 'dark-theme', чтобы не осталось следов.
        document.body.classList.remove('dark-theme');
        // Текст кнопки – переключение на тёмную тему.
        themeToggle.textContent = '🌙 Тёмная';
    }

    // ---------- 3. ОБРАБАТЫВАЕМ КЛИК ПО КНОПКЕ ----------
    // Навешиваем обработчик события 'click' на кнопку.
    themeToggle.addEventListener('click', () => {
        // Метод toggle добавляет класс 'dark-theme', если его нет, и удаляет, если он есть.
        // Таким образом, одним вызовом мы переключаем тему.
        document.body.classList.toggle('dark-theme');
        
        // Проверяем, активна ли сейчас тёмная тема (присутствует ли класс у body).
        const isDark = document.body.classList.contains('dark-theme');
        
        // Сохраняем текущий выбор в localStorage, чтобы при следующем заходе на сайт
        // тема автоматически восстановилась.
        // Если isDark === true, записываем 'dark', иначе 'light'.
        localStorage.setItem('theme', isDark ? 'dark' : 'light');
        
        // Меняем надпись на кнопке в зависимости от нового состояния.
        // Если сейчас тёмная тема – кнопка предлагает перейти на светлую (☀️ Светлая),
        // если светлая – на тёмную (🌙 Тёмная).
        themeToggle.textContent = isDark ? '☀️ Светлая' : '🌙 Тёмная';
    });
})();   // Круглые скобки в конце означают немедленный вызов функции сразу после её объявления.